def operacao (valor1,valor2):
    resultado_soma=valor1+valor2
    resultado_diferenca=valor1-valor2
    resultado_multip=valor1+valor2
    resultado_div=valor1/valor2
    resultado_rest=valor1%valor2

    print("\n Soma: {}"
          " \n Diferença: {}"
          " \n Multiplicação: {}"
          " \n Divisão: {}"
          " \n Resto: {}" .format(resultado_soma,
                                  resultado_diferenca,
                                  resultado_multip,
                                  resultado_div,
                                  resultado_rest))
